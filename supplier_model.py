class Supplier:

    def __init__(
        self,
        supplier_id,
        supplier_name,
        contact_person,
        phone,
        email,
        address,
        created_date
    ):
        self.supplier_id = supplier_id
        self.supplier_name = supplier_name
        self.contact_person = contact_person
        self.phone = phone
        self.email = email
        self.address = address
        self.created_date = created_date