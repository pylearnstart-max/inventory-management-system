def employee_dashboard():

    while True:

        print("\n===================================")
        print("       EMPLOYEE DASHBOARD")
        print("===================================")

        print("1. Product View")
        print("2. Customer View")
        print("3. Sales Entry")
        print("4. Order View")
        print("5. Dashboard")
        print("6. Logout")

        print("===================================")


        choice = input("Enter Your Choice : ")


        if choice == "1":

            print("Product View Module")


        elif choice == "2":

            print("Customer View Module")


        elif choice == "3":

            print("Sales Entry Module")


        elif choice == "4":

            print("Order View Module")


        elif choice == "5":

            print("Dashboard Module")


        elif choice == "6":

            print("\nLogout Successful")
            break


        else:

            print("\nInvalid Choice")